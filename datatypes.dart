void main() {
  // Integer
  int age = 25;
  print('Age: $age');

  // Double
  double height = 5.9;
  print('Height: $height');

  // String
  String name = 'John Doe';
  print('Name: $name');

  // Boolean
  bool isStudent = true;
  print('Is Student: $isStudent');

  // List
  List<String> fruits = ['Apple', 'Banana', 'Cherry'];
  print('Fruits: $fruits');

  // Map
  Map<String, String> capitalCities = {
    'USA': 'Washington, D.C.',
    'France': 'Paris',
    'Japan': 'Tokyo'
  };
  print('Capital Cities: $capitalCities');

  // Set
  Set<int> uniqueNumbers = {1, 2, 3, 3, 4};
  print('Unique Numbers: $uniqueNumbers');
}