void main() {
  var name = "Abeni";
  // name = 10; // we can not change the data type of a variable

  print(name);

  final age = 23;
  // age = 30; // we can not change the value of a final variable
  print(age);

  const isOpen = true;
  // isOpen = false; // we can not change the value of a const variable
  print(isOpen);
}
